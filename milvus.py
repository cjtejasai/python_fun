def create_date():
    # Define the number of data elements
    num_data = 2000

    # Generate a list of random data
    data_json = []

    for i in range(num_data):
        # Generate a random text
        text = ''.join(random.choices('abcdefghijklmnopqrstuvwxyz ', k=500))
        # Generate an empty vector array of size 1536
        vector_array = np.zeros((1536,))
        # Create a dictionary with the data and vector array
        data_dict = {'pdf_id': i, 'pdf_name': f'document{i}',
                     "pdf_content": text, "pdf_vectors": vector_array}
        # Add the dictionary to the list of data
        data_json.append(data_dict)
    return data_json


def create_collection():
    pdf_id = FieldSchema(
        name="pdf_id",
        dtype=DataType.INT64,
        is_primary=True,
    )
    pdf_name = FieldSchema(
        name="pdf_name",
        dtype=DataType.VARCHAR,
        max_length=200
    )
    pdf_content = FieldSchema(
        name="pdf_content",
        dtype=DataType.VARCHAR,
        max_length=65300
    )
    pdf_vectors = FieldSchema(
        name="pdf_vectors",
        dtype=DataType.FLOAT_VECTOR,
        dim=1536
    )
    schema = CollectionSchema(
        fields=[pdf_id, pdf_name, pdf_content, pdf_vectors],
        description="pdfs insertion"
    )
    collection_name = "pdf_search"
    from pymilvus import Collection
    collection = Collection(
        name=collection_name,
        schema=schema,
        using='default',
        shards_num=2
    )


